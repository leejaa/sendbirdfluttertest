class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key});

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  static const platform = MethodChannel('samples.flutter.dev/battery');
  String _batteryLevel = 'Unknown battery level.';

  Future<void> _getBatteryLevel() async {
    String batteryLevel;
    try {
      final result = await platform.invokeMethod<int>('getBatteryLevel');
      batteryLevel = 'Battery level at $result % .';
    } on PlatformException catch (e) {
      batteryLevel = "Failed to get battery level: '${e.message}'.";
    }

    setState(() {
      _batteryLevel = batteryLevel;
    });
  }

  Future<void> _liveinit() async {
    await platform.invokeMethod('liveinit');
  }

  Future<void> _authenticate() async {
    await platform.invokeMethod('authenticate');
  }

  Future<void> _createlive() async {
    await platform.invokeMethod('createlive');
  }

  @override
  Widget build(BuildContext context) {
    return Material(
      child: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.spaceEvenly,
          children: [
            ElevatedButton(
              onPressed: _getBatteryLevel,
              child: const Text('Get Battery Level'),
            ),
            // Text(_batteryLevel),
            ElevatedButton(
              onPressed: _liveinit,
              child: const Text('liveinit'),
            ),
            ElevatedButton(
              onPressed: _authenticate,
              child: const Text('authenticate'),
            ),
            ElevatedButton(
              onPressed: _authenticate,
              child: const Text('createlive'),
            ),
          ],
        ),
      ),
    );
  }
}
